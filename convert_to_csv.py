import csv


def convert_travellers(travellers):
    travellers = frozenset(travellers)
    if travellers == {'Brett'}:
        return 'Brett'
    elif travellers == {'Andrea'}:
        return 'Andrea'
    elif travellers == {'Andrea', 'Brett'}:
        return 'Andrea & Brett'

csv_rows = [('year', 'purpose', 'travellers', 'city', 'country', 'latitude',
             'longitude', 'arrived', 'departed', 'photos', 'comment')]
for year in sorted(TRIPS.keys()):
    trips = TRIPS[year]
    print(year, len(trips))
    # ..., comment, photos
    for trip in trips:
        travellers = convert_travellers(trip['travellers'])
        trip_details = year, trip['purpose'], travellers
        for city in trip['cities']:
            city_location = CITIES[city['key']]
            city_details = (city_location['name'], city_location['country'],
                            city_location['lat'], city_location['lng'],
                            city['arrived'], city['left'],
                            trip.get('photos'), trip.get('comment'))
            csv_rows.append(trip_details + city_details)

with open('travelogue.csv', 'w', encoding='utf-8') as file:
    writer = csv.writer(file)
    writer.writerows(csv_rows)
